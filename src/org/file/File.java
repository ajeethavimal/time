package org.file;
public class File {
public static void main(String[] args) {
	int a=10,b=20,c=a;
	System.out.println("**************");
	System.out.println("Before Swapping");
	System.out.println("a is"+a);
	System.out.println("b is"+b);
	a=b;
	b=c;
	System.out.println("After Swapping");
	System.out.println("a is"+a);
	System.out.println("b is"+b);
	System.out.println("**************");
	
}
}
